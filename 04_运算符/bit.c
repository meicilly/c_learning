#include <stdio.h>

int main(void){
      //按位与&
    //     7654 3210
    //0x5a:0101 1010
    //0xe3:1110 0011
    //&-------------
    //     0100 0010    -> 0x42
    printf("0x5a & 0xe3的结果是%#x\n", 0x5a & 0xe3);
    //0x44:0100 0100
    //0xc1:1100 0001
    //&-------------
    //     0100 0000    -> 0x40
    printf("0x44 & 0xc1的结果是%#x\n", 0x44 & 0xc1);
    
    //按位或:| 
    //     7654 3210
    //0x5a:0101 1010
    //0xe3:1110 0011
    //|-------------
    //     1111 1011    -> 0xfb
    printf("0x5a | 0xe3的结果是%#x\n", 0x5a | 0xe3);
    //0x44:0100 0100
    //0xc1:1100 0001
    //|-------------
    //     1100 0101    -> 0xc5
    printf("0x44 | 0xc1的结果是%#x\n", 0x44 | 0xc1);
    
    //按位异或:^ 
    //     7654 3210
    //0x5a:0101 1010
    //0xe3:1110 0011
    //^-------------
    //     1011 1001    -> 0xb9
    printf("0x5a ^ 0xe3的结果是%#x\n", 0x5a ^ 0xe3);
    //0x44:0100 0100
    //0xc1:1100 0001
    //^-------------
    //     1000 0101    -> 0x85
    printf("0x44 ^ 0xc1的结果是%#x\n", 0x44 ^ 0xc1);
    printf("%#x\n", ~0x5a);//0x5a=0101 1010  -> 1010 0101 -> 0xa5
    printf("%#hhx\n", ~0x5a);
    return 0;
}